print(6)  # Imprime um número inteiro positivo
print(-8)  # Imprime um número inteiro negativo
print(8.290)  # Imprime um número de ponto flutuante positivo (float)
print(-8.290)  # Imprime um número de ponto flutuante negativo (float)

print(4 + 2)  # Soma de dois números inteiros
print(4 + 2.345)  # Soma entre um inteiro e um float
print(2 * 2 + 5)  # Multiplicação com soma (ordem das operações)
print(2 * (2 + 5))  # Parênteses mudam a ordem da operação

num1 = 52  # Define a variável num1 com valor inteiro
num2 = 10  # Define a variável num2 com valor inteiro
print(num1 + num2)  # Soma entre duas variáveis
print(num1 - num2)  # Subtração entre duas variáveis

print(str(num1) + " é o meu número da sorte")  # Converte número para string e concatena com texto

print(min(2, 8))  # Retorna o menor valor entre os dois números
print(max(2, 8))  # Retorna o maior valor entre os dois números
print(pow(3, 3))  # Calcula 3 elevado à 3 (potência)
print(round(11.7))  # Arredonda o número para o inteiro mais próximo