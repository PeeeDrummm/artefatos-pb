num1 = input("Digite um número: ")  # Solicita o primeiro número ao usuário (como string)
num2 = input("Digite outro número: ")  # Solicita o segundo número ao usuário (como string)

resultado = float(num1) + float(num2)  # Converte as strings para float e realiza a soma

print("A soma dos valores informados é: " + str(resultado))  # Converte o resultado para string e exibe a mensagem